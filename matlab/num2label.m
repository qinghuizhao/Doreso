function label = num2label(num)

switch num
case 1
    label = 'pop';
case 2
    label = 'blues';
case 3
    label ='classical';
case 4
    label ='country';
case 5
    label = 'disco';
case 6 
    label = 'hiphop';
case 7 
    label = 'jazz';
case 8 
    label ='metal';
case 9 
    label = 'rock';
otherwise
    label = 'reggea';
end

end 
